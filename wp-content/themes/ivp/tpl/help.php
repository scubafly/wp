<h5>Custom Features</h5>
<p>You can use the following placeholders:</p>
<ul>
    <li><strong>[vacatures]</strong> to display the vacatures list</li>
    <li><strong>[contact-form]</strong> to display the contact-form</li>
    <li><strong>[newsletter-form]</strong> to display the contact-form</li>
    <li><strong>[site-url]</strong> to display the site url</li>
    <li><strong>[news limit="1"]</strong> to display the last news entry</li>
    <li><strong>[news]</strong> to display all the news entries</li>
    <li><strong>[media-list type="employees"]</strong>  to display employee list</li>
</ul>

<p>You can use the custom variables (see at the bottom of the edit page/post):</p>
<ul>
    <li><strong>is_en_index</strong> with value <strong>1</strong> to set the home english page (the other english section page must have this page as a parent)</li>
    <li><strong>is_nl_index</strong> with value <strong>1</strong> to set the home nl page (the other nl section page can have any parent)</li>
</ul>